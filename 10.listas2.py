num= [99,34,25,56,72]
print(num)
num[1]= 88 #permite cambiar el valor de una posicion.
print(num)
#FUNCION INSERT: permite insertar un valor en la posicion deseada
num.insert(1,77)
print(num)
# FUNSION APPEND: permite adicionar valores a la lista 
num.append(100)
print(num)
num2= [9,8,7]
print(num)
#FUNCION EXTEND
num.extend(num2)
print(num)
#FUNCION REMOVE:
num.remove(100)
print(num)
#FUNCION POP: permite borar un dato de una posicion o indice
num.pop(2)
print(num)
#FUCION DEL : permite borrar un valor de una posicion o indice 
del num[0]
print(num)
#FUNCION CLEAR:
num2.clear()
print(num2)
